CREATE TABLE RESERVA (
    ID_RESERVA NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    ID_HUESPED NUMBER NOT NULL,
    ID_HABITACION NUMBER NOT NULL,

    FECHA_RESERVA DATE DEFAULT SYSDATE NOT NULL,
    FECHA_INGRESO DATE NOT NULL,
    FECHA_SALIDA DATE NOT NULL,

    ESTADO_RESERVA VARCHAR2(20) DEFAULT 'CONFIRMADA' NOT NULL,

    FECHA_CREACION DATE DEFAULT SYSDATE,
    FECHA_MODIFICACION DATE,

    CONSTRAINT CHK_RESERVA_ESTADO
        CHECK (ESTADO_RESERVA IN (
            'CONFIRMADA',
            'CANCELADA',
            'FINALIZADA',
            'PENDIENTE'
        )),

    CONSTRAINT CHK_RESERVA_FECHAS CHECK (FECHA_SALIDA > FECHA_INGRESO),

    CONSTRAINT FK_RESERVA_HUESPED
        FOREIGN KEY (ID_HUESPED)
        REFERENCES HUESPED(ID_HUESPED),

    CONSTRAINT FK_RESERVA_HABITACION
        FOREIGN KEY (ID_HABITACION)
        REFERENCES HABITACION(ID_HABITACION)
);