CREATE TABLE RESERVAS (
    ID_RESERVA NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    ID_HUESPED NUMBER NOT NULL,
    ID_HABITACION NUMBER NOT NULL,

    FECHA_ENTRADA DATE NOT NULL,
    FECHA_SALIDA DATE NOT NULL,

    ESTADO_RESERVA VARCHAR2(20) DEFAULT 'CONFIRMADA' NOT NULL,
    ESTADO_REGISTRO VARCHAR2(15) DEFAULT 'ACTIVO' NOT NULL,

    FECHA_CREACION DATE DEFAULT SYSDATE NOT NULL,
    FECHA_ACTUALIZACION DATE,

    CONSTRAINT CHK_RESERVA_FECHAS
        CHECK (FECHA_ENTRADA < FECHA_SALIDA),

    CONSTRAINT CHK_RESERVA_ESTADO
        CHECK (ESTADO_RESERVA IN (
            'CONFIRMADA',
            'EN_CURSO',
            'FINALIZADA',
            'CANCELADA'
        )),

    CONSTRAINT CHK_RESERVA_REGISTRO
        CHECK (ESTADO_REGISTRO IN (
            'ACTIVO',
            'ELIMINADO'
        ))
);