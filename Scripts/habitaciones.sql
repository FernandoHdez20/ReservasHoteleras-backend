CREATE TABLE HABITACION (
    ID_HABITACION NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    NUMERO NUMBER(10) NOT NULL,
    TIPO VARCHAR2(50) NOT NULL,
    PRECIO NUMBER(10,2) NOT NULL,
    CAPACIDAD NUMBER(3) NOT NULL,
    ESTADO_HABITACION VARCHAR2(20) DEFAULT 'DISPONIBLE' NOT NULL,
    ESTADO_REGISTRO VARCHAR2(20) DEFAULT 'ACTIVO' NOT NULL,

    FECHA_CREACION DATE DEFAULT SYSDATE,
    FECHA_MODIFICACION DATE,

    CONSTRAINT CHK_HAB_NUMERO_POSITIVO CHECK (NUMERO > 0),

    CONSTRAINT CHK_HAB_PRECIO_POSITIVO CHECK (PRECIO > 0),

    CONSTRAINT CHK_HAB_CAPACIDAD_MINIMA CHECK (CAPACIDAD >= 1),

    CONSTRAINT CHK_HAB_ESTADO_HABITACION CHECK (ESTADO_HABITACION IN ('DISPONIBLE','OCUPADA','MANTENIMIENTO','RESERVADA')),

    CONSTRAINT CHK_HAB_ESTADO_REGISTRO CHECK (ESTADO_REGISTRO IN ('ACTIVO','INACTIVO'))
);