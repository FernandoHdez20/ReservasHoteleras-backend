CREATE TABLE USUARIOS(
    ID_USUARIO NUMBER(10) GENERATED BY DEFAULT AS IDENTITY,
    USERNAME VARCHAR2(20) NOT NULL,
    PASSWORD VARCHAR2(20) NOT NULL,
    ROL VARCHAR(20) NOT NULL,
    ESTADO_REGISTRO VARCHAR2(30) NOT NULL,
    CONSTRAINT USUARIO_PK PRIMARY KEY (ID_USUARIO),
    CONSTRAINT chk_password_alfanumerico CHECK (
        NOT REGEXP_LIKE(PASSWORD, '[^a-zA-Z0-9]')
        AND REGEXP_LIKE(PASSWORD, '[a-zA-Z]')
        AND REGEXP_LIKE(PASSWORD, '[0-9]')
    )
);